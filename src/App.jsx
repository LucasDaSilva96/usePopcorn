const average = (arr) => {
  const result = arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);
  return Number(result).toFixed(1);
};

const API_KEY = "af877589";

export default function App() {
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState(tempWatchedData);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  async function fetchMovies(title) {
    setIsLoading(true);
    setError("");
    try {
      const url = await fetch(
        `http://www.omdbapi.com/?apikey=${API_KEY}&s=${title}`
      );

      if (!url.ok)
        throw new Error(
          "Failed to fetch the movie, check your network-connection and try again"
        );

      const res = await url.json();

      if (res.Response === "False") throw new Error("Movie not found");

      setMovies(res.Search);
    } catch (err) {
      setError(err.message);
    } finally {
      setIsLoading(false);
    }
  }

  function handleSelectMovie(id) {
    return setSelectedId((selectedId) => (id === selectedId ? null : id));
  }

  function handleCloseMovie() {
    return setSelectedId(null);
  }

  useEffect(
    function () {
      if (query.length < 3) {
        setMovies([]);
        setError("");
        return;
      }
      fetchMovies(query);
    },
    [query]
  );

  return (
    <React.Fragment>
      <NavBar>
        <SearchBar query={query} setQuery={setQuery}></SearchBar>
        <NumResult movies={movies}></NumResult>
      </NavBar>

      <Main>
        <Box>
          {isLoading && <Loader></Loader>}
          {!isLoading && !error && (
            <MovieList
              movies={movies}
              onSelectMovie={handleSelectMovie}
            ></MovieList>
          )}
          {error && <ErrorMessage message={error}></ErrorMessage>}
        </Box>

        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              handleCloseMovie={handleCloseMovie}
            ></MovieDetails>
          ) : (
            <React.Fragment>
              <WatchedSummary watched={watched}></WatchedSummary>
              <WatchedMovieList watched={watched}></WatchedMovieList>
            </React.Fragment>
          )}
        </Box>
      </Main>
    </React.Fragment>
  );
}

